             <h3>Baiting </h3>
                <p>Baiting is an effective way of controlling rats and mice. In this, an attractive food for rats is mixed with poison. We use special anti-coagulant rodenticides mixed with a bait material that is very attractive to rodents. This method is more effective as rodents do not die immediately, but about four days after feeding. Therefore, the remaining rats do not know that it is a poison and do not become bait-shy and the entire population of rats starts eating the bait. Baits need to be checked and replaced periodically to ensure that sufficient bait is available for all the rats to eat and die.</p>

              <h3>Trapping</h3> 
                <p>Snap traps and glue boards can be useful in situations using poison baits can be hazardous – like pharmaceutical units, food processing units, etc. This also prevents the possibility of a rat dying in an inaccessible place from where it cannot be easily removed. This method is effective when the place is infested with a limited number of rodents.</p>

              <h3>Burrow Fumigation</h3> 
